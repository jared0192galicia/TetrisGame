package clases;

public class Score {
	
	public static int time;
	public static int points;
	public static int experiense;

	public static int getTime() {
		return time;
	}
	public static void setTime(int time) {
		Score.time = time;
	}
	public static int getPoints() {
		return points;
	}
	public static void setPoints(int points) {
		Score.points = points;
	}
	public static int getExperiense() {
		return experiense;
	}
	public static void setExperiense(int experiense) {
		Score.experiense = experiense;
	}
	
}
